# Функция zip() применяется к набору итерируемых объектов и 
# возвращает итератор с кортежами из элементов входных данных

# zip([1, 2, 3], ['о', 'д', 'т'], ['f', 's', 't']) => 
# [(1, 'о', 'f'), (2, 'д', 's'), (3, 'т', 't')]

# Если на входе будут списки разных размеров, то, соответственно, 
# в итоговом списке будет столько кортежей, сколько элементов 
# в минимальном из них

